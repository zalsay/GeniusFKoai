export function normalizeLanguage(
  language: string | null | undefined,
): Language {
  return LANGUAGE_OPTIONS.some((option) => option.value === language)
    ? (language as Language)
    : DEFAULT_LANGUAGE;
}

export function getStoredLanguage(): Language {
  if (typeof localStorage === "undefined") return DEFAULT_LANGUAGE;
  return normalizeLanguage(localStorage.getItem(LANGUAGE_STORAGE_KEY));
}

export function translate(
  key: TranslationKey,
  language: Language = DEFAULT_LANGUAGE,
  params?: Record<string, string | number>,
) {
  let text = MESSAGE_MAP[language]?.[key] || ZH_MESSAGES[key] || key;
  if (params) {
    Object.entries(params).forEach(([name, value]) => {
      text = text.replaceAll(`{${name}}`, String(value));
    });
  }
  return text;
}

export function translateAccountStatus(
  status: string,
  language: Language = DEFAULT_LANGUAGE,
) {
  const key = STATUS_KEYS[status];
  return key ? translate(key, language) : status;
}

export function translateChoiceLabel(
  value: string,
  fallback: string,
  language: Language = DEFAULT_LANGUAGE,
) {
  const key = CHOICE_KEYS[value];
  return key ? translate(key, language) : fallback;
}

export function formatDate(
  value: string | number | Date,
  language: Language,
  options?: Intl.DateTimeFormatOptions,
) {
  return new Date(value).toLocaleDateString(language, options);
}

export function formatDateTime(
  value: string | number | Date,
  language: Language,
  options?: Intl.DateTimeFormatOptions,
) {
  return new Date(value).toLocaleString(language, options);
}
